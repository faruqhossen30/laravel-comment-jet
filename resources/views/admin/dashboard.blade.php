    <script>
        function toggleSignInSignUpModals(e) {
            var currentModalName = "default-modal";
            var currentModalElem = document.getElementById(currentModalName);

            const options = {
                placement: 'center',
                backdrop: 'static',
                closable: true,
                onHide: function() {},
                onShow: function() {
                    console.log('working');
                },
                onToggle: function() {}
            };

            var currentModalObj = new Modal(currentModalElem, options);
            currentModalObj.toggle();
        }


        function showModal() {
            var myModal = document.getElementById('modalEl');
            const options = {
                placement: 'top-center',
                backdrop: 'dynamic',
                backdropClasses: 'bg-gray-900/50 dark:bg-gray-900/80 fixed inset-0 z-40',
                closable: true,
                onHide: () => {
                    console.log('modal is hidden');
                },
                onShow: () => {
                    console.log('modal is shown');
                },
                onToggle: () => {
                    console.log('modal has been toggled');
                },
            };

            var myModalObject = new Modal(myModal, options);
            myModalObject.show();

        }


    </script>
